import math


# define a multiply function

def mult(num1, num2):
    return num1 * num2


# define a dived function

def div(num1, num2):
    return num1 / num2


# define a multiply function

def sub(num1, num2):
    return num1 - num2


# define a multiply function

def add(num1, num2):
    return num1 + num2


# define area of a circle function

def area_of_circle(num1, num2):
    return (num1 * num2) * math.pi


def area_of_triangle(num1, num2):
    return 0.5 * (num1 * num2)



